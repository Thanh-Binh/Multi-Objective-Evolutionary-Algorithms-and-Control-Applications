function g=m96g7(x)
% g=m96g7(x)
% Test Case #7 of Michalevicz 1996
%   'Evolutionary Algorithms for Constrainted Parameter Optimization Prob.'
%
% Objective function
%  f=x(1)^2 + x(2)^2 + x(1)*x(2) - 14*x(1) - 16*x(2) + (x(3)-10)^2 +... 
%  4*(x(4)-5)^2 + (x(5)-3)^2 + 2*(x(6)-1)^2 + 5*x(7)^2 + 7*(x(8)-11)^2 +...
%  2*(x(9)-10)^2 + (x(10)-7)^2 + 45;
% subject to the constraints:
%  105-4*x(1) - 5*x(2) + 3*x(7) - 9*x(8) >= 0
%  -10*x(1) + 8*x(2) + 17*x(7) - 2*x(8) >= 0
%  8*x(1) - 2*x(2) - 5*x(9) + 2*x(10) + 12 >= 0
%  3*x(1) - 6*x(2) - 12*(x(9)-8)^2 + 7*x(10) >= 0
%  -3*(x(1)-2)^2 - 4*(x(2)-3)^2 - 2*x(3)^2 + 7*x(4) +120 >= 0
%  -x(1)^2 - 2*(x(2)-2)^2 + 2*x(1)*x(2) - 14*x(5) + 6*x(6) >= 0
%  -5*x(1)^2 - 8*x(2) - (x(3)-6)^2 + 2*x(4) + 40 >= 0
%  -.5*(x(1)-8)^2 - 2*(x(2)-4)^2 - 3*x(5)^2 + x(6) + 30 >= 0
% and bounds
%     -10 <= x <= 10
%
% Global Minimum is:
%   x=(2.172 2.364 8.774 5.096 .9907 1.4306 1.322 9.829 8.280 8.376)
% and fmin=24.3062

% All Rights Reserved, 08-July-1996  
% To Thanh Binh IFAT Uni. of Magdeburg Germany


epsilon=1e-7; beta=2;

if all(abs(x) <= 10),

   g=zeros(2,1);
   gcons(1) =105-4*x(1) - 5*x(2) + 3*x(7) - 9*x(8) ;
   gcons(2)= -10*x(1) + 8*x(2) + 17*x(7) - 2*x(8);
   gcons(3)= 8*x(1) - 2*x(2) - 5*x(9) + 2*x(10) + 12;
   gcons(4)= 3*x(1) - 6*x(2) - 12*(x(9)-8)^2 + 7*x(10); 
   gcons(5)= -3*(x(1)-2)^2 - 4*(x(2)-3)^2 - 2*x(3)^2 + 7*x(4) +120;
   gcons(6)= -x(1)^2 - 2*(x(2)-2)^2 + 2*x(1)*x(2) - 14*x(5) + 6*x(6);
   gcons(7)= -5*x(1)^2 - 8*x(2) - (x(3)-6)^2 + 2*x(4) + 40;
   gcons(8)= -.5*(x(1)-8)^2 - 2*(x(2)-4)^2 - 3*x(5)^2 + x(6) + 30;

   g(1)=sum((abs(gcons).*(gcons < -epsilon)).^beta);
   g(2)=x(1)^2 + x(2)^2 + x(1)*x(2) - 14*x(1) - 16*x(2) + (x(3)-10)^2 +... 
      4*(x(4)-5)^2 + (x(5)-3)^2 + 2*(x(6)-1)^2 + 5*x(7)^2 + 7*(x(8)-11)^2 +...
      2*(x(9)-10)^2 + (x(10)-7)^2 + 45;

else,
   g=inf;
end
